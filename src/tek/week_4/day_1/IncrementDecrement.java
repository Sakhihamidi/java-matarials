package tek.week_4.day_1;

public class IncrementDecrement {

			public static void main(String[] args) {
							
				//increment
				int anyNumber = 10; 
				System.out.println(anyNumber++);
				System.out.println(anyNumber++);
				System.out.println(anyNumber++);
				System.out.println("-----------------------------------------------");
				// decrement

				System.out.println(anyNumber--);
				System.out.println(anyNumber--);
				System.out.println(anyNumber--);
				System.out.println("-----------------------------------------------");
				//pre increment

				System.out.println(++anyNumber);
				System.out.println(++anyNumber);
				System.out.println(++anyNumber);
				System.out.println("-----------------------------------------------");
				//pre decrement

				System.out.println(--anyNumber);
				System.out.println(--anyNumber);
				System.out.println(--anyNumber);
				
				
			
	}

}
