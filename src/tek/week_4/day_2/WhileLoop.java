package tek.week_4.day_2;

public class WhileLoop {
	public static void main(String[] args) {
		String [] countries = {"United States", "Spain", "London", "Indian"};
		int index = 0;
	
		
		// While loop
		
		while(index<countries.length){
			System.out.println(index + " " + countries[index]);
			index++;

		}

}
	
}
