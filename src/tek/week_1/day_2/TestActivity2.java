package tek.week_1.day_2;

public class TestActivity2 {
	// shortcut for main method is: write main + ctrl + space
	public static void main(String[] args) {
		
		int x = 10;
		
// now value of x is 10
		
		System.out.println(x);
		System.out.println(x+20);
		System.out.println(x--);
		System.out.println(--x);
		System.out.println(x--);
		System.out.println(x);
		
		x *= 5;
		System.out.println(x);
		
		x /= 4;
		System.out.println(x);
	}
}
